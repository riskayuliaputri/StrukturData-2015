public class Vector3D
{
    
    double x,y,z;
    
    public double x()
    {
        // initialise instance variables
        return x;
    }
    
    public double y()
    {
        // initialise instance variables
        return y;
    }
    
    public double z()
    {
        // initialise instance variables
        return z ;
    }
    
    public Vector3D (double x, double y, double z){
        this.x=x;
        this.y=y;
        this.z=z;
    
    }

}
